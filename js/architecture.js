function drawComponent(x, y, w, h, id, title, color, desc) {
    const g = document.createElementNS('http://www.w3.org/2000/svg', 'g');
    g.id = `arch_${id}`;
    g.style.cursor = 'pointer';
    
    const rect = createSVGRect(x, y, w, h, 8, 'rgba(15, 23, 42, 0.85)', color, 2);
    const text = createSVGText(x + w/2, y + h/2 + 5, title, '#fff', 15);
    
    g.appendChild(rect); g.appendChild(text);
    g.addEventListener('mouseenter', (e) => showTooltip(e, title, desc));
    g.addEventListener('mouseleave', hideTooltip);
    svg.appendChild(g);
    return g;
}

function drawProcess(x, y, title, color, desc, isBg) {
    const g = document.createElementNS('http://www.w3.org/2000/svg', 'g');
    g.id = `proc_${title}`;
    g.style.cursor = 'pointer';
    
    const circle = document.createElementNS('http://www.w3.org/2000/svg', 'circle');
    circle.setAttribute('cx', x); circle.setAttribute('cy', y);
    circle.setAttribute('r', isBg ? 28 : 38);
    circle.setAttribute('fill', 'rgba(15,23,42,0.95)');
    circle.setAttribute('stroke', color);
    circle.setAttribute('stroke-width', 2);
    
    const text = createSVGText(x, y + 5, title, '#fff', isBg ? 13 : 15);
    
    g.appendChild(circle); g.appendChild(text);
    g.addEventListener('mouseenter', (e) => showTooltip(e, title, desc));
    g.addEventListener('mouseleave', hideTooltip);
    svg.appendChild(g);
}

function drawDisk(x, y, id, title, color, desc) {
    const g = document.createElementNS('http://www.w3.org/2000/svg', 'g');
    g.id = `disk_${id}`;
    g.style.cursor = 'pointer';
    
    const path = document.createElementNS('http://www.w3.org/2000/svg', 'path');
    path.setAttribute('d', `M${x},${y+20} A55,15 0 0,0 ${x+110},${y+20} L${x+110},${y+90} A55,15 0 0,1 ${x},${y+90} Z`);
    path.setAttribute('fill', 'rgba(15,23,42,0.9)');
    path.setAttribute('stroke', color);
    path.setAttribute('stroke-width', 2);
    
    const top = document.createElementNS('http://www.w3.org/2000/svg', 'ellipse');
    top.setAttribute('cx', x+55); top.setAttribute('cy', y+20);
    top.setAttribute('rx', 55); top.setAttribute('ry', 15);
    top.setAttribute('fill', 'rgba(15,23,42,0.9)');
    top.setAttribute('stroke', color);
    top.setAttribute('stroke-width', 2);
    
    const text = createSVGText(x + 55, y + 60, title, '#fff', 13);
    
    g.appendChild(path); g.appendChild(top); g.appendChild(text);
    g.addEventListener('mouseenter', (e) => showTooltip(e, title, desc));
    g.addEventListener('mouseleave', hideTooltip);
    svg.appendChild(g);
}

export function updateArchitecture(state) {
    const fillPercent = Math.min(state.redoBufferLevel / 100, 1);
    redoFillBar.setAttribute('width', 710 * fillPercent);
    if(fillPercent > 0.8) {
        redoFillBar.setAttribute('fill', 'rgba(239, 68, 68, 0.9)');
    } else {
        redoFillBar.setAttribute('fill', 'rgba(239, 68, 68, 0.6)');
    }
}

export function animateComponentGlow(targetId, type) {
    const el = document.getElementById(targetId);
    if(el) {
        el.classList.add(`glow-${type === 'write' ? 'red' : 'green'}`);
        setTimeout(() => el.classList.remove(`glow-${type === 'write' ? 'red' : 'green'}`), 800);
    }
}

export function shootArchParticle(pathName, duration = 600) {
    const pInfo = archPaths[pathName];
    if(!pInfo) return;
    
    const particle = document.createElementNS('http://www.w3.org/2000/svg', 'circle');
    particle.setAttribute('r', 6);
    particle.setAttribute('fill', pInfo.color);
    particle.setAttribute('filter', `drop-shadow(0 0 8px ${pInfo.color})`);
    svg.appendChild(particle);
    
    particles.push({
        el: particle, startX: pInfo.x1, startY: pInfo.y1, endX: pInfo.x2, endY: pInfo.y2,
        startTime: performance.now(), duration: duration, progress: 0
    });
}

function renderArchParticles(time) {
    particles = particles.filter(p => {
        p.progress = (time - p.startTime) / p.duration;
        if(p.progress >= 1) {
            if(p.el.parentNode) svg.removeChild(p.el);
            return false;
        }
        const ease = 1 - Math.pow(1 - p.progress, 3);
        const currX = p.startX + (p.endX - p.startX) * ease;
        const currY = p.startY + (p.endY - p.startY) * ease;
        
        p.el.setAttribute('cx', currX);
        p.el.setAttribute('cy', currY);
        return true;
    });
    requestAnimationFrame(renderArchParticles);
}
