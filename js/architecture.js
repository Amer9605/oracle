    sgaGroup.appendChild(createSVGText(755, 40, 'System Global Area (SGA)', '#93c5fd', 18, 'middle', 'bold'));
    svg.appendChild(sgaGroup);
    
    // SGA Subcomponents
    drawComponent(400, 70, 260, 100, 'SharedPool', 'Shared Pool', '#3b82f6', 'Library Cache (Parsed SQL), Data Dictionary Cache.');
    drawComponent(680, 70, 150, 100, 'LargePool', 'Large Pool', '#6366f1', 'UGA, I/O buffers for RMAN/Parallel.');
